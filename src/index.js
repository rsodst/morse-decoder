const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    return expr.replace(/\s/g, '').replace(/(\*+)/g, '*').split('*')
        .map(p => p.match(/.{1,10}/gm).reduce((output, current) => {
            return output
                .concat(MORSE_TABLE[
                    current
                    .match(/.{1,2}/gm)
                    .map(p => p == '10' ? '.' : p == '11' ? '-' : undefined)
                    .join('')]);
        }, []).join(''))
        .join(' ');
}

module.exports = {
    decode
}